import Foundation
extension UIFont
{
    class func 	customSummaryLabels() -> UIFont
    {
        return UIFont(name: "HelveticaNeue-Light", size: 12)!
    }
    
    class func 	customSummaryValues() -> UIFont
    {
        return UIFont(name: "HelveticaNeue-Light", size: 16)!
    }
}